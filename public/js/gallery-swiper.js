/**
 * USE-CASE: Создание слайдера для галереи достижений пользователя
 * Цель: Пользователь может листать свои успехи в формировании привычек
 */
document.addEventListener('DOMContentLoaded', function() {
    // Инициализация Swiper с конфигурацией
    const swiper = new Swiper('.swiper', {
        // Параметр: loop
        // Назначение: Зацикливание слайдов для бесконечной прокрутки
        // Значение: true - слайды циклически повторяются
        loop: true,

        // Параметр: spaceBetween
        // Назначение: Отступ между слайдами в пикселях
        // Значение: 15 - создает визуальное разделение между карточками достижений
        spaceBetween: 15,

        // Параметр: slidesPerView
        // Назначение: Количество видимых слайдов одновременно
        // Значение: 1 - показываем одно достижение за раз для фокусировки внимания
        slidesPerView: 1,

        // Параметр: autoplay
        // Назначение: Автоматическая прокрутка слайдов
        // Конфигурация:
        //   delay: 3000 - смена слайда каждые 3 секунды
        //   disableOnInteraction: false (по умолчанию) - автопрокрутка продолжается после взаимодействия
        autoplay: {
            delay: 3000
        },

        // Параметр: navigation
        // Назначение: Добавление кнопок навигации (вперед/назад)
        // Конфигурация:
        //   nextEl: '.swiper-button-next' - CSS-селектор для кнопки "следующий слайд"
        //   prevEl: '.swiper-button-prev' - CSS-селектор для кнопки "предыдущий слайд"
        navigation: {
            nextEl: '.swiper-button-next',
            prevEl: '.swiper-button-prev'
        }
    });
});
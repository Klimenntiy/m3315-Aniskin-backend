/**
 * USE-CASE: Визуализация прогресса формирования привычки за 5 недель
 * Цель: Пользователь видит динамику роста своего прогресса
 */
document.addEventListener('DOMContentLoaded', function() {
    const progressChart = document.getElementById('progressChart');
    if (progressChart) {
        // Инициализация линейного графика Chart.js
        new Chart(progressChart.getContext('2d'), {
            // Параметр: type
            // Назначение: Тип графика
            // Значение: 'line' - линейный график для отображения тренда во времени
            type: 'line',

            // Параметр: data
            // Назначение: Данные для отображения на графике
            data: {
                // Массив меток для оси X (недели)
                labels: ['Нед1', 'Нед2', 'Нед3', 'Нед4', 'Нед5'],

                // Массив наборов данных (в нашем случае один набор - прогресс)
                datasets: [{
                    // Название набора данных (отображается в легенде)
                    label: 'Прогресс %',

                    // Массив значений для оси Y (процент выполнения)
                    data: [30, 45, 60, 75, 85],

                    // Цвет линии графика (зеленый оттенок)
                    borderColor: '#81a684',

                    // Цвет заливки под линией с прозрачностью 10%
                    backgroundColor: 'rgba(129, 166, 132, 0.1)',

                    // Толщина линии графика в пикселях
                    borderWidth: 2,

                    // Заполнять область под линией цветом
                    fill: true
                }]
            },

            // Параметр: options
            // Назначение: Конфигурация внешнего вида и поведения графика
            options: {
                // Адаптивность под размер контейнера
                responsive: true,

                // Отключение сохранения пропорций (растягиваем на весь контейнер)
                maintainAspectRatio: false,

                // Настройки плагинов
                plugins: {
                    // Конфигурация легенды графика
                    legend: {
                        display: true  // Показывать легенду с label
                    }
                },

                // Настройки осей X и Y
                scales: {
                    // Ось X (недели)
                    x: {
                        ticks: {
                            // Настройка шрифта для меток оси X
                            font: {
                                size: 10  // Меньший размер для компактности
                            }
                        }
                    },

                    // Ось Y (проценты)
                    y: {
                        // Начинать ось с 0 для корректного отображения прогресса
                        beginAtZero: true,

                        // Максимальное значение оси (100% - полное выполнение)
                        max: 100,

                        ticks: {
                            // Настройка шрифта для меток оси Y
                            font: {
                                size: 9  // Еще меньший размер для цифр процентов
                            }
                        }
                    }
                }
            }
        });
    }

    /**
     * USE-CASE: Визуализация недельной статистики выполнения привычек
     * Цель: Пользователь видит процентное соотношение выполненных/пропущенных привычек
     */
    const weeklyChart = document.getElementById('weeklyChart');
    if (weeklyChart) {
        // Инициализация кольцевой диаграммы Chart.js
        new Chart(weeklyChart.getContext('2d'), {
            // Параметр: type
            // Назначение: Тип диаграммы
            // Значение: 'doughnut' - кольцевая диаграмма (donut chart)
            type: 'doughnut',

            // Параметр: data
            // Назначение: Данные для секторов диаграммы
            data: {
                // Массив названий категорий (отображаются в легенде)
                labels: ['Выполнено', 'Пропущено', 'В процессе'],

                // Массив наборов данных (один набор для круговой диаграммы)
                datasets: [{
                    // Массив значений для каждого сектора
                    data: [65, 20, 15],

                    // Массив цветов для каждого сектора:
                    // '#81a684' - зеленый (выполнено)
                    // '#e74c3c' - красный (пропущено)
                    // '#f5b971' - оранжевый (в процессе)
                    backgroundColor: ['#81a684', '#e74c3c', '#f5b971']
                }]
            },

            // Параметр: options
            // Назначение: Конфигурация внешнего вида диаграммы
            options: {
                // Адаптивность под размер контейнера
                responsive: true,

                // Отключение сохранения пропорций
                maintainAspectRatio: false,

                // Настройки плагинов
                plugins: {
                    // Конфигурация легенды
                    legend: {
                        display: true  // Показывать легенду с названиями категорий
                    }
                },

                // Параметр: cutout
                // Назначение: Процент вырезанной середины (создает кольцо)
                // Значение: '70%' - диаметр отверстия 70% от диаметра диаграммы
                // Эффект: Превращает pie chart в donut chart для лучшей читаемости
                cutout: '70%'
            }
        });
    }
});